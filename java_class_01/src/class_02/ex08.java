package class_02;

public class ex08 {

	public static void main(String[] args) {
		System.out.printf("정수: %d\n", 100);
		System.out.printf("실수: %f\n", 3.14);
		System.out.printf("문자: %c\n", 'A');
		System.out.printf("뮨자열: %s\n", "Hellc");
		System.out.println();
		System.out.printf("정수: %5d\n", 123);
		System.out.printf("실수: %-5d\n", 123);
		System.out.printf("문자: %.2f\n", 12.1234);

	}

}
